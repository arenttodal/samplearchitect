/* ksp-gen.js — KSP script generator (CRITICAL PATH)                */
/* Generates UI knobs + control handlers + effects chain.            */
/* Zone mapping is handled by Kontakt's Mapping Editor (auto-map).   */
/*                                                                   */
/* All knob ranges: 0 to 1000000 (Kontakt native).                   */
/* No scaling math — values passed directly to set_engine_par().     */
/* Constants verified against KSP 6.0.2 Reference Manual.           */

function generateKSP(samples, stats, config) {
  var lines = [];
  var mapped = samples.filter(function(s) { return s.parsed; });
  var instrumentName = stats.instrument || 'Instrument';
  var maxRR = stats.maxRoundRobins;

  var enabledControls = getEnabledControls();
  var enabledEffects = getEnabledEffects();

  // Skip EQ — $EFFECT_TYPE_PARA_EQ is inconsistent across Kontakt versions
  var activeEffects = enabledEffects.filter(function(e) { return e.key !== 'eq'; });

  lines.push('{ ' + instrumentName + ' - Generated by SampleArchitect }');
  lines.push('');
  lines.push('');
  lines.push('on init');
  lines.push('  set_script_title("' + instrumentName + ' - SampleArchitect")');
  lines.push('  message("' + instrumentName + ' | ' + enabledControls.length + ' controls | SampleArchitect")');

  // UI Controls
  if (enabledControls.length > 0) {
    lines.push('');
    lines.push('  { UI Controls }');
  }

  enabledControls.forEach(function(item) {
    var key = item.key;
    lines.push('');

    if (key === 'volume') {
      lines.push('  declare ui_knob $Volume (0, 1000000, 1)');
      lines.push('  $Volume := 750000');
      lines.push('  set_knob_defval($Volume, 750000)');
      lines.push('  set_knob_label($Volume, "Vol")');
      lines.push('  make_persistent($Volume)');
    } else if (key === 'pan') {
      lines.push('  declare ui_knob $Pan (0, 1000000, 1)');
      lines.push('  $Pan := 500000');
      lines.push('  set_knob_defval($Pan, 500000)');
      lines.push('  set_knob_label($Pan, "Pan")');
      lines.push('  make_persistent($Pan)');
    } else if (key === 'attack') {
      lines.push('  declare ui_knob $Attack (0, 1000000, 1)');
      lines.push('  $Attack := 0');
      lines.push('  set_knob_defval($Attack, 0)');
      lines.push('  set_knob_label($Attack, "Atk")');
      lines.push('  make_persistent($Attack)');
    } else if (key === 'release') {
      lines.push('  declare ui_knob $Release (0, 1000000, 1)');
      lines.push('  $Release := 350000');
      lines.push('  set_knob_defval($Release, 350000)');
      lines.push('  set_knob_label($Release, "Rel")');
      lines.push('  make_persistent($Release)');
    } else if (key === 'tune') {
      lines.push('  declare ui_knob $Tune (0, 1000000, 1)');
      lines.push('  $Tune := 500000');
      lines.push('  set_knob_defval($Tune, 500000)');
      lines.push('  set_knob_label($Tune, "Tune")');
      lines.push('  make_persistent($Tune)');
    } else if (key === 'cutoff') {
      lines.push('  declare ui_knob $Cutoff (0, 1000000, 1)');
      lines.push('  $Cutoff := 800000');
      lines.push('  set_knob_defval($Cutoff, 800000)');
      lines.push('  set_knob_label($Cutoff, "Cut")');
      lines.push('  make_persistent($Cutoff)');
    } else if (key === 'resonance') {
      lines.push('  declare ui_knob $Resonance (0, 1000000, 1)');
      lines.push('  $Resonance := 200000');
      lines.push('  set_knob_defval($Resonance, 200000)');
      lines.push('  set_knob_label($Resonance, "Res")');
      lines.push('  make_persistent($Resonance)');
    } else if (key === 'reverb') {
      lines.push('  declare ui_knob $Reverb (0, 1000000, 1)');
      lines.push('  $Reverb := 300000');
      lines.push('  set_knob_defval($Reverb, 300000)');
      lines.push('  set_knob_label($Reverb, "Rvb")');
      lines.push('  make_persistent($Reverb)');
    }
  });

  // Effects Chain (verified working in Kontakt 6)
  if (activeEffects.length > 0) {
    lines.push('');
    lines.push('  { Effects Chain }');

    activeEffects.forEach(function(item) {
      if (item.key === 'filter') {
        // Filter in group insert slot 0
        lines.push('  set_engine_par($ENGINE_PAR_EFFECT_TYPE, $EFFECT_TYPE_FILTER, 0, 0, -1)');
      } else if (item.key === 'reverb') {
        // Reverb in instrument insert slot 0
        lines.push('  set_engine_par($ENGINE_PAR_EFFECT_TYPE, $EFFECT_TYPE_REVERB, -1, 0, 1)');
      } else if (item.key === 'delay') {
        // Delay in instrument insert slot 1
        lines.push('  set_engine_par($ENGINE_PAR_EFFECT_TYPE, $EFFECT_TYPE_DELAY, -1, 1, 1)');
      }
    });
  }

  // Round robin counter declaration (inside on init)
  if (maxRR > 1) {
    lines.push('');
    lines.push('  { Round Robin }');
    lines.push('  declare $rr_counter');
    lines.push('  $rr_counter := 0');
  }

  lines.push('end on');

  // UI Control Handlers — values passed directly, no scaling
  if (enabledControls.length > 0) {
    lines.push('');

    enabledControls.forEach(function(item) {
      var key = item.key;
      lines.push('');
      if (key === 'volume') {
        lines.push('on ui_control ($Volume)');
        lines.push('  set_engine_par($ENGINE_PAR_VOLUME, $Volume, -1, -1, -1)');
        lines.push('end on');
      } else if (key === 'pan') {
        lines.push('on ui_control ($Pan)');
        lines.push('  set_engine_par($ENGINE_PAR_PAN, $Pan, -1, -1, -1)');
        lines.push('end on');
      } else if (key === 'attack') {
        lines.push('on ui_control ($Attack)');
        lines.push('  set_engine_par($ENGINE_PAR_ATTACK, $Attack, -1, -1, -1)');
        lines.push('end on');
      } else if (key === 'release') {
        lines.push('on ui_control ($Release)');
        lines.push('  set_engine_par($ENGINE_PAR_RELEASE, $Release, -1, -1, -1)');
        lines.push('end on');
      } else if (key === 'tune') {
        lines.push('on ui_control ($Tune)');
        lines.push('  set_engine_par($ENGINE_PAR_TUNE, $Tune, -1, -1, -1)');
        lines.push('end on');
      } else if (key === 'cutoff') {
        lines.push('on ui_control ($Cutoff)');
        lines.push('  set_engine_par($ENGINE_PAR_CUTOFF, $Cutoff, 0, 0, -1)');
        lines.push('end on');
      } else if (key === 'resonance') {
        lines.push('on ui_control ($Resonance)');
        lines.push('  set_engine_par($ENGINE_PAR_RESONANCE, $Resonance, 0, 0, -1)');
        lines.push('end on');
      } else if (key === 'reverb') {
        lines.push('on ui_control ($Reverb)');
        lines.push('  set_engine_par($ENGINE_PAR_INSERT_EFFECT_OUTPUT_GAIN, $Reverb, -1, 0, 1)');
        lines.push('end on');
      }
    });
  }

  // Round Robin Cycling
  if (maxRR > 1) {
    lines.push('');
    lines.push('');
    lines.push('on note');
    lines.push('  $rr_counter := ($rr_counter + 1) mod ' + maxRR);
    lines.push('  ');
    lines.push('  declare $g');
    lines.push('  $g := 0');
    lines.push('  while ($g < ' + maxRR + ')');
    lines.push('    if ($g = $rr_counter)');
    lines.push('      allow_group($g)');
    lines.push('    else');
    lines.push('      disallow_group($g)');
    lines.push('    end if');
    lines.push('    $g := $g + 1');
    lines.push('  end while');
    lines.push('end on');
  }

  lines.push('');

  return lines.join('\n');
}
