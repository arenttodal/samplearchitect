/* ksp-gen.js — KSP script generator (CRITICAL PATH) */
/* Generates UI knobs + control handlers only.                     */
/* Zone mapping is handled by Kontakt's Mapping Editor (auto-map). */

function generateKSP(samples, stats, config) {
  var lines = [];
  var mapped = samples.filter(function(s) { return s.parsed; });
  var instrumentName = stats.instrument || 'Instrument';
  var maxRR = stats.maxRoundRobins;

  lines.push('{ ' + instrumentName + ' - Generated by SampleArchitect }');
  lines.push('');
  lines.push('');
  lines.push('on init');
  lines.push('  set_script_title("' + instrumentName + ' - SampleArchitect")');
  lines.push('  message("' + instrumentName + ' | SampleArchitect")');

  // UI Controls
  var enabledControls = getEnabledControls();

  if (enabledControls.length > 0) {
    lines.push('');
    lines.push('  { -- UI Controls -- }');
  }

  enabledControls.forEach(function(item) {
    var key = item.key;
    var cfg = item.config;
    var defVal = cfg.default;

    lines.push('');

    if (key === 'volume') {
      lines.push('  declare ui_knob $Volume (0, 100, 1)');
      lines.push('  $Volume := ' + defVal);
      lines.push('  set_knob_defval($Volume, $Volume)');
      lines.push('  set_knob_label($Volume, "Vol")');
      lines.push('  make_persistent($Volume)');
    } else if (key === 'pan') {
      lines.push('  declare ui_knob $Pan (-100, 100, 1)');
      lines.push('  $Pan := 0');
      lines.push('  set_knob_defval($Pan, 0)');
      lines.push('  set_knob_label($Pan, "Pan")');
      lines.push('  make_persistent($Pan)');
    } else if (key === 'attack') {
      lines.push('  declare ui_knob $Attack (0, 100, 1)');
      lines.push('  $Attack := ' + defVal);
      lines.push('  set_knob_defval($Attack, $Attack)');
      lines.push('  set_knob_label($Attack, "Atk")');
      lines.push('  make_persistent($Attack)');
    } else if (key === 'release') {
      lines.push('  declare ui_knob $Release (0, 100, 1)');
      lines.push('  $Release := ' + defVal);
      lines.push('  set_knob_defval($Release, $Release)');
      lines.push('  set_knob_label($Release, "Rel")');
      lines.push('  make_persistent($Release)');
    } else if (key === 'tune') {
      lines.push('  declare ui_knob $Tune (-36, 36, 1)');
      lines.push('  $Tune := 0');
      lines.push('  set_knob_defval($Tune, 0)');
      lines.push('  set_knob_label($Tune, "Tune")');
      lines.push('  make_persistent($Tune)');
    } else if (key === 'cutoff') {
      lines.push('  declare ui_knob $Cutoff (0, 100, 1)');
      lines.push('  $Cutoff := ' + defVal);
      lines.push('  set_knob_defval($Cutoff, $Cutoff)');
      lines.push('  set_knob_label($Cutoff, "Cut")');
      lines.push('  make_persistent($Cutoff)');
    } else if (key === 'resonance') {
      lines.push('  declare ui_knob $Resonance (0, 100, 1)');
      lines.push('  $Resonance := ' + defVal);
      lines.push('  set_knob_defval($Resonance, $Resonance)');
      lines.push('  set_knob_label($Resonance, "Res")');
      lines.push('  make_persistent($Resonance)');
    } else if (key === 'reverb') {
      lines.push('  declare ui_knob $ReverbSend (0, 100, 1)');
      lines.push('  $ReverbSend := ' + defVal);
      lines.push('  set_knob_defval($ReverbSend, $ReverbSend)');
      lines.push('  set_knob_label($ReverbSend, "Rvb")');
      lines.push('  make_persistent($ReverbSend)');
    }
  });

  // Round robin counter declaration (inside on init)
  if (maxRR > 1) {
    lines.push('');
    lines.push('  { -- Round Robin -- }');
    lines.push('  declare $rr_counter');
    lines.push('  $rr_counter := 0');
  }

  lines.push('end on');

  // UI Control Handlers
  if (enabledControls.length > 0) {
    lines.push('');
    lines.push('');
    lines.push('{ -- UI Control Handlers -- }');

    enabledControls.forEach(function(item) {
      var key = item.key;
      lines.push('');
      if (key === 'volume') {
        lines.push('on ui_control ($Volume)');
        lines.push('  set_engine_par($ENGINE_PAR_VOLUME, $Volume * 6300, -1, -1, -1)');
        lines.push('end on');
      } else if (key === 'pan') {
        lines.push('on ui_control ($Pan)');
        lines.push('  set_engine_par($ENGINE_PAR_PAN, $Pan * 10, -1, -1, -1)');
        lines.push('end on');
      } else if (key === 'attack') {
        lines.push('on ui_control ($Attack)');
        lines.push('  set_engine_par($ENGINE_PAR_ATTACK, $Attack * 10000, -1, -1, -1)');
        lines.push('end on');
      } else if (key === 'release') {
        lines.push('on ui_control ($Release)');
        lines.push('  set_engine_par($ENGINE_PAR_RELEASE, $Release * 10000, -1, -1, -1)');
        lines.push('end on');
      } else if (key === 'tune') {
        lines.push('on ui_control ($Tune)');
        lines.push('  set_engine_par($ENGINE_PAR_TUNE, $Tune * 1000, -1, -1, -1)');
        lines.push('end on');
      } else if (key === 'cutoff') {
        lines.push('on ui_control ($Cutoff)');
        lines.push('  set_engine_par($ENGINE_PAR_CUTOFF, $Cutoff * 10000, 0, 0, -1)');
        lines.push('end on');
      } else if (key === 'resonance') {
        lines.push('on ui_control ($Resonance)');
        lines.push('  set_engine_par($ENGINE_PAR_RESONANCE, $Resonance * 10000, 0, 0, -1)');
        lines.push('end on');
      } else if (key === 'reverb') {
        lines.push('on ui_control ($ReverbSend)');
        lines.push('  set_engine_par($ENGINE_PAR_SEND_EFFECT_0_LEVEL, $ReverbSend * 6300, -1, -1, -1)');
        lines.push('end on');
      }
    });
  }

  // Round Robin Cycling
  if (maxRR > 1) {
    lines.push('');
    lines.push('');
    lines.push('{ -- Round Robin Cycling -- }');
    lines.push('');
    lines.push('on note');
    lines.push('  $rr_counter := ($rr_counter + 1) mod ' + maxRR);
    lines.push('  ');
    lines.push('  declare $g');
    lines.push('  $g := 0');
    lines.push('  while ($g < ' + maxRR + ')');
    lines.push('    if ($g = $rr_counter)');
    lines.push('      allow_group($g)');
    lines.push('    else');
    lines.push('      disallow_group($g)');
    lines.push('    end if');
    lines.push('    $g := $g + 1');
    lines.push('  end while');
    lines.push('end on');
  }

  lines.push('');

  return lines.join('\n');
}
